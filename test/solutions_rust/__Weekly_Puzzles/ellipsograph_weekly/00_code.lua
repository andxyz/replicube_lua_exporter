--[[ Ever heard of the Trammel of Archimedes?
	Look up a video! In this simplified version,
	The GREY dot stays halfway between the BLUE 
	and GREEN dots. BLUE and GREEN only ever move
	in straight lines, but GREY makes a circle!
	
	This model helps to visualize
	how sin() and cos() connect circles to 
	oscillating (wave-like) behavior.
	
	The GREEN voxel shows how the height of the
	white voxel changes over time (modeled by sin(t))
	
	The BLUE voxel shows the horizontal component of
	the white voxel over time (modeled by cos(t))
	
	Remember that the default period of sin(t) and cos(t)
	is 2*pi, so to change the wave pattern to match the
	duration of the animation, multiply the input by 2*pi,
	then divide the input by the number of frames.
	
